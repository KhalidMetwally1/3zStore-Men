//[ACTIVE ON SCROLL CODE]
let sections = document.querySelectorAll('section'); //storing sections
let navLinks = document.querySelectorAll('header .navBar ul li a'); //to add .active class

window.onscroll = () => {
    sections.forEach(sec => {
        let top = window.scrollY; //the Y-Coordenates while scrolling
        let offset = sec.offsetTop - 100; //Sections' Y-Coordenates
        let height = sec.offsetHeight; //Sections' Height
        let id = sec.getAttribute('id'); //storing sections' ids like (bySportsName) section

        if(top >= offset && top < offset + height){ // Means only if we r between section's start and end
            navLinks.forEach(links => {
                links.classList.remove('active'); //removes the old active section
                document.querySelector('header .navBar ul li a[href*=' + id + ']').classList.add('active'); ////adds the new active section
            });
        };
    });
};
